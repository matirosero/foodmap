					<h3>Información general</h3>

					<p>
						Bhakri (Dhebra) es un pan redondo y plano, sin levar, utilizado habitualmente en la cocina del estado de Maharashtra en India pero también es habitual en el Oeste y Centro del país, especialmente en los estados de Rajasthan, Gujarat, Malwa, Goa, y norte de Karnataka. Es más rústico que un roti. Puede ser suave o duro en su textura.

					</p>
					<p>
						Siendo un pan básico, bhakri se sirve con cuajada, chutney, vegetales y arroz. Está hecho en la mayoría de casos a partir de harina de trigo, harina de jowar flour, harina de bajra, harina de nachni, e incluso de harina de arroz (en la región de Konkan). Bhakris se hacen principalmente con agua caliente y harina.
