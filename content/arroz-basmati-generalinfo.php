					<h3>Información general</h3>

					<p>
						El basmati (Hindi:bāsmatī) es una <strong>variedad de arroz</strong> caracterizada por tener un <strong>grano largo</strong>, y es famoso por sus delicadas fragancias y su exquisito sabor. Su nombre en Hindi significa: <strong>"reina de las fragancias"</strong>.
					</p>

					<p>
						Los granos del arroz basmati son mucho más largos que anchos, y se hacen incluso más largos mientras se cocinan. Durante la cocción permanecen juntos y se separan poco, <strong>no son pegajosos después de cocinar</strong>. El arroz basmati está disponible en dos modalidades: <strong>blanco y moreno</strong>. Ambos se cocinan en intervalos de tiempo que no superan los 20 minutos. Dada la capacidad que tienen los granos de arroz de permanecer juntos debido a la alta cantidad de almidón, muchos cocineros lo lavan antes de cocinarlo. Si se remoja entre una media hora a dos horas antes de cocinar, los granos serán menos propensos a romperse durante la cocción.
					</p>

					<p>
						El arroz basmati <strong>se ha cultivado en la India y en Pakistán durante centenares de años</strong>. En las colinas del Himalaya se tiene muy buena reputación en la producción del mejor basmati. No obstante se cultiva un buen basmati en la India en una variedad denominada Dehra Dun. <strong>El arroz de Patna</strong> es un primo cercano del arroz basmati que se cultiva cerca de Patna en Bihar. Los mejores tipos de arroz basmati se envejecen por varios años antes de ser descascarillados y comercializados, ya que <strong>el arroz se cocina mejor con un contenido de agua más bajo y, de esta manera, se concentra su aroma</strong>.
					</p>

