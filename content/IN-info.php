					<h4>Información general</h4>

					<p>
						La India (oficialmente la <strong>República de la India</strong>) es un país ubicado en Asia del Sur. Tiene más de 1240 millones de habitantes siendo el <strong>segundo más poblado del mundo después de China</strong>. Su superficie es de 3,3 millones de Km2 que supone ser el séptimo país más extenso. Limita con el océano Índico al sur, con el mar Arábigo al oeste y con el golfo de Bengala al este. La India limita con Pakistán al oeste; al norte con China, Nepal y Bután y al este con Bangladés y Birmania. Se encuentra cerca de las islas de Sri Lanka, Maldivas e Indonesia. <strong>Su capital es Nueva Delhi</strong> y su ciudad más poblada es <strong>Bombay</strong>.
					</p>
					
					<h4>Cultura</h4>

					<p>
						La cultura de la India está marcada por un <strong>alto grado de sincretismo y pluralismo</strong>. Los indios han logrado conservar sus tradiciones previamente establecidas, mientras que absorben nuevas costumbres, tradiciones e ideas de los invasores y los inmigrantes, al mismo tiempo que extienden su influencia cultural a otras partes de Asia, principalmente Indochina y el Extremo Oriente.
					</p>
					<p>
						<strong>Muchas de las celebraciones indias tienen un origen religioso</strong>, aunque algunas se celebran independientemente de la casta o credo. Algunas de las fiestas más populares en el país son: <strong>el Diwali, el Holi, el Durga Puja, Eid ul-Fitr, Eid al-Adha, Navidad y el Vesak</strong>. Además de éstas, India tiene tres días festivos nacionales: el Día de la República, el Día de la Independencia y el Gandhi Jayanti. Otra serie de días festivos, variando entre nueve y doce días, se celebran oficialmente en cada uno de los estados de la república. Las prácticas religiosas son parte integral de la vida cotidiana y son un asunto de interés público.
					</p>
					<p>
						<strong>La indumentaria tradicional india varía en sus colores y estilos según la región</strong> y depende de varios factores, incluyendo el clima. Los estilos populares de vestido incluyen prendas sencillas como el <strong>sari para las mujeres y el dhoti para hombres</strong>; también son populares otras prendas como el salwar kameez para las mujeres y los kurta–pyjamas, los pantalones de estilo europeo y las camisas para los hombres.
					</p>

