					<h4>Información general</h4>

					<p>
						La India (oficialmente la <strong>República de la India</strong>) es un país ubicado en Asia del Sur. Tiene más de 1240 millones de habitantes siendo el <strong>segundo más poblado del mundo después de China</strong>. Su superficie es de 3,3 millones de km<sup>2</sup> que supone ser el séptimo país más extenso. Limita con el océano Índico al sur, con el mar Arábigo al oeste y con el golfo de Bengala al este. La India limita con Pakistán al oeste; al norte con China, Nepal y Bután y al este con Bangladés y Birmania. Se encuentra cerca de las islas de Sri Lanka, Maldivas e Indonesia. <strong>Su capital es Nueva Delhi</strong> y su ciudad más poblada es <strong>Bombay</strong>.
					</p>
					
					<h4>Cultura</h4>

					<p>
						La cultura de la India está marcada por un <strong>alto grado de sincretismo y pluralismo</strong>. Los indios han logrado conservar sus tradiciones previamente establecidas, mientras que absorben nuevas costumbres, tradiciones e ideas de otros países y de la influencia de poblaciones inmigrantes. De la misma manera extienden su cultura a otras partes de Asia, principalmente Indochina y el Extremo Oriente.
					</p>
					
					<p>
						La India es el lugar de nacimiento de las llamadas religiones dármicas: <strong>hinduismo, budismo, jainismo y sijismo</strong>. En la actualidad, hinduismo y budismo son, respectivamente, la tercera y la cuarta religión más practicadas en el mundo, teniendo entre ambas cerca de 1400 millones de seguidores en todo el planeta. La India es uno de los países con mayor diversidad religiosa en el mundo, con una de las sociedades y culturas más profundamente religiosas. <strong>La religión juega un papel central</strong> y definitivo en la vida de la nación y de mucha de su gente.
					</p>
					
					<p>
						Por esta razón <strong>la mayoría de sus celebraciones tienen un origen religioso</strong>, aunque existan algunas que se celebran independientemente de la casta o credo. Las fiestas más populares son: <strong>el Diwali, el Holi, el Durga Puja, Eid ul-Fitr, Eid al-Adha, Navidad y el Vesak</strong>. Además de éstas, India tiene tres días festivos nacionales: el Día de la República, el Día de la Independencia y el Gandhi Jayanti. 
					</p>
					
					<p>
						Debido a su diversidad regional, el gran número de idiomas existentes en la India ha provocado la aparición de diferentes culturas (existen 216 idiomas, cada uno de ellos hablado por grupos de 10 000 o más personas). La gran mayoría de estos idiomas proceden de <strong>dos familias lingüísticas fundamentales: la rama dravidiana y la rama indoaria</strong>, la primera concentrada en la zona del sur y la segunda más presente en el norte. La Constitución de la India estipula que <strong>el hindi y el inglés son los idiomas oficiales</strong> del Gobierno nacional. Son también oficiales un total de 22 idiomas, utilizados básicamente por los gobiernos regionales. El sánscrito es el idioma clásico de la India y el sureste de Asia, y posee la misma relevancia que el latín o griego en Europa.  
					</p>
										
					<p>
						<strong>La indumentaria tradicional india varía en sus colores y estilos según la región</strong> y depende de varios factores, incluyendo el clima. Los estilos populares de vestido incluyen prendas sencillas como el <strong>sari para las mujeres y el dhoti para hombres</strong>; también son populares otras prendas como el salwar kameez para las mujeres y los kurta–pyjamas, los pantalones de estilo europeo y las camisas para los hombres.
					</p>

