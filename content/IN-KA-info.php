					<h4>Información general</h4>

					<p>Karnataka es uno de los estados del <strong>sur de la India</strong>. Se cree que el origen de <strong>su nombre</strong> proviene de la unión de los términos <i>karu</i> y <i>nadu</i> que significan <strong>"tierra elevada"</strong>.</p>

					<p>Antes de 1973 se le conocía como <strong>Estado de Mysore</strong> y estaba formado por el antiguo reino del mismo nombre. En 1956, se incluyeron también algunas zonas anexas en las que el idioma principal era el <i>kannada</i>, ahora lengua oficial.</p>

					<p><strong>La capital de Karnataka es la ciudad de Bangalore</strong>, única población del estado que supera el millón de habitantes. Otras ciudades importantes son Mysore, Mangalore, Hubli.Dharwad, Bellary y Belgaum. La región limita con el Mar Arábigo al oeste, Goa al noroeste, Maharastra al norte, Andhra Pradesh al este, Tamil Nadu al este y sudeste y con Kerala al sudoeste.</p>


					</p>

					<h4>Cultura</h4>

					<p>Las etnias nativas que poseen <strong>diferentes identidades linguísticas y relogiosas</p> de Karnataka combinado con sus antiguas historias ha contribuido de manera importante a la <strong>herencia cultural variada del estado</strong>. Además de <strong>Kannadigas</strong>, Karnataka es hogar de <strong>Tuluvas, Kodavas y Konkanis</strong>. Las poblaciones menores de <strong>Budistas Tibetanos</strong> y las tribus como los Soligas, Yeravas, Todas y Siddhis también viven en Karnataka.</p>

					<p> <strong>El arte folcrólico tradicional incluye el amplio abanico de música, danza, drama y narrativa por grupos itinerantes.</strong> Yakshagana de Malnad y la costa de Karnataka, una clásica pieza dramática de danza, es una de las mayores obras teatrales de la región. Su <strong>cultura teatral contemporánea</strong> se mantiene viva con organizaciones como Ninasam, Ranga Shankara, Rangayana y Prabhat Kalavidaru, que continúan construyendo la base sobre Gubbi Veeranna, T. P. Kailasam, B. V. Karanth, K V Subbanna, Prasanna y  otros artistas.</p>

