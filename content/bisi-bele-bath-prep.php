					<h3>Elaboración</h3>

					<h4>Pasos</h4>

						<ul style="margin-top:10px">
							<ol>
								<li>
								<strong>Para hacer el polvo de especias, calentar una pequeña sartén a fuego medio; añadir el channa dal y el urad dal</strong>. Remover continuamente hasta que estén doradas y se pueda sentir el aroma a tostado.
								</li>
							
								<li>
								<strong>Una vez que las lentejas están doradas, añadir la canela en rama, el clavo, las vainas de cardamono, los chilis secos rojos, las semillas de alholva y coriandro</strong>. Bajar el fuego y dorar hasta que los chilis se tuesten y se sienta el aroma de las especies.
								</li>
							
								<li>
								A continuación, <strong>añadir las semillas de amapola y el coco</strong>. Seguir dorando con el resto de ingredientes unos 30-40 segundos hasta que el coco pierda la humedad. Una vez esté dorado, añadir los polvos de cúrcuma y asafoetida. Remover hasta que se mezcle bien con el resto de ingredientes. Apagar el fuego y dejar enfriar la mezcla completamente.
								</li>
							
								<li>
								Una vez frío, <strong>añadir los ingredientes en un molinillo de especias y hacer con la mezcla un polvo fino</strong>. Colocar la mezcla en un bol y esperar a incorporarlo al resto de la receta.
								</li>

								<li>
								En una olla a presión grande, <strong>añadir el agua de tamarindo, la panela, las piezas de moringa, las cebollas perla, guisantes, zanahorias, pimiento verde, tomates, sal y finalmente el polvo de especias bisi bele bhaat</strong>.
								</li>

							
								<li>
								Remover bien los ingredientes. Tapar la olla a presión y <strong>dejar que los vegetales se cocinen hasta que escuche 2 silbidos y apagar el fuego</strong>. dado que los vegetales se cocinan muy rápido, pondremos la olla a presión cerrada bajo el agua fría para que se elimine la presión. Este proceso permite bajar la presión rápidamente de manera inmediata.
								</li>
							

								<li>
								Abrir la olla a presión y <strong>sentir el aroma del curry vegetal</strong>. Colocar el contenido en otro bol para poder usar la olla a presión en la siguiente etapa, si no tiene otra. 
								</li>

							
								<li>
								En la olla a presión <strong>agregar el arroz y las lentejas lavados previamente, el polvo de cúrcuma, sal y 5 tazas de agua</strong>. Tapar la olla y ponerla a fuego alto, dejando que el arroz se cueza hasta que se escuchen 2 silbidos.
								</li>

							
								<li>
								<strong>Luego de escuchar los silbidos, bajar el fuego y dejarlo allí durante 5 minutos</strong>. Después de transcurrido este tiempo, apagar el fuego y dejar que la presión se elimine naturalmente. El arroz se termina de cocer en este tiempo, por lo tanto esperar a que la presión se libere completamente.
								</li>

							
								<li>
								Calentar una pequeña sartén a fuego medio. <strong>Añadir una cucharada de té de ghee y dejar que se derrita</strong>. Añadir las semillas de mostaza y de comino. Dejar que crujan. Luego añadir el chili rojo seco, las hojas de curry y cocer hasta que se doren un poco. Remover para que los ingredientes se doren bien. Una vez dorados, separar del fuego.
								</li>

								<li>
								Ahora volvemos al arroz y a la olla a presión. Una vez que se haya eliminado la presión, abrir la olla a presión. <strong>Añadir el Curry picante con vegetales</strong>. Remover todos los ingredientes juntos hasta que se mezclen bien y la preparación se vea como una mezcla uniforme. En este momento añadir el condimiento preparado y dos cucharaditas de café más de ghee. Una vez más, remover bien la mezcla.
								</li>

								<li>
								Colocar el plato ya preparado en una bandeja para servir. <strong>Esparcir las hojas de coriandro picadas y los boondi fritos en la parte superior</strong>.
								</li>

								<li>
								Servir con raita de tomate y cebolla para comer o cenar.
								</li>

							</ol>
						</ul>


