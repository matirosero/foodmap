					<h3>Información general</h3>

					<p>
						El arroz (del árabe <i>Ar-ruzz</i>) es la semilla de la planta <i>Oryza sativa</i>. Se trata de un <strong>cereal</strong> considerado <strong>alimento básico en muchas culturas</strong> (en especial la cocina asiática y en algunas partes de América Latina). El arroz es el <strong>segundo cereal más producido en el mundo</strong>, tras el maíz, debido a que se utilizad con otros propósitos además del consumo humano como alimento.
					</p>
					<p>
						Constituye <strong>el cereal más importante en la alimentación</strong> y contribuye de forma muy efectiva al aporte calórico de la dieta actual. Un dato interesante es que es <strong>fuente de una quinta parte de las calorías consumidas en el mundo</strong>.
					</p>

