					<h4>Información del arroz en India</h4>

					<p>
						El <strong>dios Shiva</strong> llamaba al arroz <strong>"Vrihi"</strong>, en sánscrito. Esto demuestra el gran vínculo de la India con uno de sus alimentos más autóctonos dado que es <strong>uno de los centros originarios del cultivo de arroz</strong> y, actualmente, <strong>la mayor zona de cultivo de arroz del mundo</strong>. Ésta se extiende por todos los estados de la India, pero <strong>Bengala Occidental, Uttar Pradesh, Madhya Pradesh, Orissa</strong> y <strong>Bihar son los principales estados productores</strong>. La cosecha temprana del Kharif dura de marzo-mayo a junio-octubre; la cosecha media del Kharif comprende desde junio-octubre hasta noviembre-febrero, y la temporada del Rabi es de noviembre-febrero a marzo-junio.
