					<h3>Información general</h3>

					<p>
						El arroz (del árabe Ar-ruzz) es la semilla de la planta Oryza sativa. Se trata de un cereal considerado alimento básico en muchas culturas culinarias (en especial la cocina asiática), así como en algunas partes de América Latina. El arroz es el segundo cereal más producido en el mundo, tras el maíz.
					</p>
					<p>
						Debido a que éste se produce con muchos otros propósitos aparte del consumo humano, se puede decir que es el arroz el cereal más importante en la alimentación humana y que contribuye de forma muy efectiva al aporte calórico de la dieta humana actual; es fuente de una quinta parte de las calorías consumidas en el mundo.
					</p>

